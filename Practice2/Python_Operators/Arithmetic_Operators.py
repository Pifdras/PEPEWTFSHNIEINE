#Arithmetic operators are used with numeric values to perform common mathematical operations:

# + Addition x+y
# - Subtraction x-y
# * Multiplication x*y
# / Division x/y
# % Modulus x%y
# ** Exponention x**y
# // Floor division x//y

x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)


#Python has two division operators:
#/ - Division (returns a float)
#// - Floor division (returns an integer)

x = 12
y = 5

print(x / y)
print(x // y)